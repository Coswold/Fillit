#include "fillit.h"

char	**blank_board(char **board, int bs)
{
	int i;
	int j;

	i = 0;
	while (i < bs)
	{
		j = 0;
		while (j < bs)
		{
			board[i][j] = '.';
			j++;
		}
		i++;
	}
	return (board);
}
